.global _start
_start:
	
	LDR R0,=array @CARGA EL INICIO DEL ARRAY
	MOV R1,#0 @VARIABLE i
	MOV R2,#10 @LIMITE DEL ARREGLO
	
	@LOOP FOR
	FOR:
		CMP R1,R2 @i<10?
		BGE DONE
		LDR R3, [R0] @CARGA EL ELEMENTO DEL ARRAY
		LSR R3,R3,#1 @DIVIDE POR 2
		STR R3,[R0]  @GUARDA EL VALOR EN MEMORIA
		ADD R0,R0,#4 @SE MUEVE AL SIGUIENTE ELEMENTO
		ADD R1,R1,#1 @AGREGA AL CONTADOR
		B FOR
	DONE:
	
.data

